    private void loadPatients() {
    try {
        DBConnection db = new DBConnection(); // ✅ Create object
        Connection con = db.connect();        // ✅ Use non-static method

        Statement stmt = con.createStatement();
        ResultSet rs = stmt.executeQuery("SELECT name FROM patients");
        cmbPatient.removeAllItems();
        while (rs.next()) {
            cmbPatient.addItem(rs.getString("name"));
        }

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error loading doctors: " + ex.getMessage());
    }
}

