    private void btnBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookActionPerformed
        String patientName = (String) cmbPatient.getSelectedItem();
    String doctorName = (String) comboDoctor.getSelectedItem();
    String time = txtTime.getText();
    String phone = txtPhone.getText();
    String date = "";

    if (dateChooser.getDate() != null) {
        date = new java.text.SimpleDateFormat("yyyy-MM-dd").format(dateChooser.getDate());
    }

    if (patientName.isEmpty() || doctorName == null || date.isEmpty() || time.isEmpty() || phone.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill all fields!");
        return;
    }

    try {
    // Create DBConnection object
    DBConnection db = new DBConnection();
    Connection con = db.connect();

    // Prepare SQL query
    String query = "INSERT INTO appointments (patient_name, doctor_name, appointment_date, appointment_time,phone_number) VALUES (?, ?, ?, ?, ?)";
    PreparedStatement pst = con.prepareStatement(query);

    pst.setString(1, patientName);
    pst.setString(2, doctorName);
    pst.setString(3, date);
    pst.setString(4, time);
    pst.setString(5, phone);
    pst.executeUpdate();
    JOptionPane.showMessageDialog(this, "Appointment Booked Successfully!");
    

// ✅ Clear fields after successful submission
cmbPatient.setSelectedIndex(0);
comboDoctor.setSelectedIndex(0);
dateChooser.setDate(null);
txtTime.setText("");
txtPhone.setText(null);
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
}

    }//GEN-LAST:event_btnBookActionPerformed

